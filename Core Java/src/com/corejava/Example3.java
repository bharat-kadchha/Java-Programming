public class Example3 {
    public static void main(String[] args) {
        byte b = 123;         //size:- 8 bit
        short s = 32767;        //size:- 16bit
        int i = 2147483647;     //size:- 32 bit
        char c = 'c';           // size:- 16bit (unicode base)
        long l = 12345L;        // size:- 64bit
        double d = 10.5;        // size:- 64bit
        float f = 10.4f;        //size:- 32bit
        boolean b1 = true;      //depends on virtual machine (JVM)



    }
}
